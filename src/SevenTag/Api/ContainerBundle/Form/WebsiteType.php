namespace SevenTag\Api\ContainerBundle\Form;

use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\FormBuilderInterface;
use Symfony\Component\OptionsResolver\OptionsResolver;
use Symfony\Component\Validator\Constraints as Assert;

/**
 * Class ContainerWebsiteType
 * @package SevenTag\Api\ContainerBundle\Form
 */
class WebsiteType extends AbstractType
{
    /**
     * @param FormBuilderInterface $builder
     * @param array $options
     */
    public function buildForm(FormBuilderInterface $builder, array $options)
    {
        $builder
            ->add('url', 'text', [
                'constraints' => [
                    new Assert\Url(),
                    new Assert\Length([
                        'min' => 3,
                        'max' => 255
                    ]),
                ]
            ])
            ->add('parameterType', 'integer');
    }

    /**
     * @param OptionsResolver $resolver
     */
    public function configureOptions(OptionsResolver $resolver)
    {
        $resolver->setDefaults([
            'data_class' => 'SevenTag\Api\ContainerBundle\Entity\ContainerWebsite'
        ]);
    }

    /**
     * @return string
     */
    public function getName()
    {
        return 'seventag_container_website_type';
    }
}
