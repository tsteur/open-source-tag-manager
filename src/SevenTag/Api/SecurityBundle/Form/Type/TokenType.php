namespace SevenTag\Api\SecurityBundle\Form\Type;

use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\FormBuilderInterface;

class TokenType extends AbstractType
{
    /**
     * {@inheritdoc}
     */
    public function buildForm(FormBuilderInterface $builder, array $options)
    {
        $builder
            ->add('grant_type', null, ['required' => true, 'description' => 'Grant type'])
            ->add('client_id', null, ['required' => true, 'description' => 'Client id'])
            ->add('client_secret', null, ['required' => true, 'description' => 'Client secret key']);
    }

    /**
     * Returns the name of this type.
     *
     * @return string The name of this type
     */
    public function getName()
    {
        return '';
    }
}
